    function getCombinedBiomeAffinity(biomeIds, type) {
      if (!biomeIds || biomeIds.length === 0) return 1.0;

      const values = biomeIds.map(id => getBiomeTypeAffinity(id, type));
      const sum = values.reduce((a, b) => a + b, 0);
      return sum / values.length;
    }

